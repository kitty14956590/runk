#include <integer.h>

/* super complicated functions */
int int_add(int x, int y) {
	return x + y;
}

int int_sub(int x, int y) {
	return x - y;
}

int int_div(int x, int y) {
	return x / y;
}

int int_mod(int x, int y) {
	return x % y;
}

int int_mul(int x, int y) {
	return x * y;
}

/* unsigned */
unsigned int uint_add(unsigned int x, unsigned int y) {
	return x + y;
}

unsigned int uint_sub(unsigned int x, unsigned int y) {
	return x - y;
}

unsigned int uint_div(unsigned int x, unsigned int y) {
	return x / y;
}

unsigned int uint_mod(unsigned int x, unsigned int y) {
	return x % y;
}

unsigned int uint_mul(unsigned int x, unsigned int y) {
	return x * y;
}
